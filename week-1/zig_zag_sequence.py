def find_zig_zag_sequence(a, n):
    a.sort()
    mid = (n + 1) // 2
    a[mid - 1], a[n - 1] = a[n - 1], a[mid - 1]

    step = mid
    end = n - 2
    while (step <= end):
        a[step], a[end] = a[end], a[step]
        step = step + 1
        end = end - 1

    for i in range(n):
        if i == n:
            print(a[i])
        else:
            print(a[i], end=' ')
    return


